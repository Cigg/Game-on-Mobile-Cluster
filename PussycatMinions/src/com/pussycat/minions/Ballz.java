package com.pussycat.minions;

public class Ballz {

	public int id;
	public int type;
	
	public float x;
	public float y;
	
	public float vx;
	public float vy;
	
	
	Ballz(int id, int type, float x, float y, float vx, float vy) {
		
		this.id = id;
		this.type = type;
		
		this.x = x;
		this.y = y;
		
		this.vx = vx;
		this.vy = vy;
		
	}
	
}
